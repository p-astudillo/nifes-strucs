# Conversion factors (Imperial to SI)
IN_TO_M = 0.0254
IN2_TO_M2 = IN_TO_M ** 2
IN3_TO_M3 = IN_TO_M ** 3
IN4_TO_M4 = IN_TO_M ** 4
IN6_TO_M6 = IN_TO_M ** 6
PLF_TO_KGM = 1.48816


def create_w_section(name: str, d: float, bf: float, tw: float, tf: float,
                      A: float, Ix: float, Sx: float, Zx: float, rx: float,
                      Iy: float, Sy: float, Zy: float, ry: float,
                      J: float, Cw: float, W: float) -> dict:
    """Create a W section dict with imperial inputs, converted to SI."""
    return {
        "name": name,
        "shape": "W",
        "standard": "AISC",
        "A": A * IN2_TO_M2,
        "Ix": Ix * IN4_TO_M4,
        "Iy": Iy * IN4_TO_M4,
        "Sx": Sx * IN3_TO_M3,
        "Sy": Sy * IN3_TO_M3,
        "Zx": Zx * IN3_TO_M3,
        "Zy": Zy * IN3_TO_M3,
        "rx": rx * IN_TO_M,
        "ry": ry * IN_TO_M,
        "J": J * IN4_TO_M4,
        "Cw": Cw * IN6_TO_M6,
        "d": d * IN_TO_M,
        "bf": bf * IN_TO_M,
        "tw": tw * IN_TO_M,
        "tf": tf * IN_TO_M,
        "W": W * PLF_TO_KGM,
        "description": f"AISC W Shape - {name}",
        "is_custom": False,
    }


