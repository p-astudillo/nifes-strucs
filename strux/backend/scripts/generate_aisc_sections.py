def generate_w_shapes() -> list[dict]:
    """Generate all W shape sections."""
    sections = []
    for data in W_SHAPES_DATA:
        section = create_w_section(*data)
        sections.append(section)
    return sections


def main() -> None:
    """Generate AISC section JSON files."""
    output_dir = Path(__file__).parent.parent / "src" / "data" / "sections"
    output_dir.mkdir(parents=True, exist_ok=True)

    # Generate W shapes
    w_shapes = generate_w_shapes()
    w_file = output_dir / "aisc_w_shapes.json"
    with w_file.open("w", encoding="utf-8") as f:
        json.dump({"sections": w_shapes}, f, indent=2)
    print(f"Generated {len(w_shapes)} W shapes to {w_file}")


if __name__ == "__main__":
    main()
